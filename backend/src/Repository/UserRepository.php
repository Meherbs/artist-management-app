    public function findOneByUsername($valueUsername): ?User
    {
        return $this->createQueryBuilder('u')
            ->where('u.username = :us')
            ->setParameter('us', $valueUsername)
            ->getQuery()
            ->getOneOrNullResult()
            ;
    }

    public function findOneBySomeField($value): ?User
    {
        return $this->createQueryBuilder('u')
            ->andWhere('u.email = :val')
            ->setParameter('val', $value)
            ->getQuery()
            ->getOneOrNullResult()
        ;
    }

}
